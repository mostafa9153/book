// ==========================================================================
// NUMBER PARSING (BENGALI & ENGLISH DIGITS)
// ==========================================================================
function parseNumber(str) {
  const bengaliDigits = {'০':0, '১':1, '২':2, '৩':3, '৪':4, '৫':5, '৬':6, '৭':7, '৮':8, '৯':9};
  let englishStr = '';
  for (let char of str) {
    if (bengaliDigits[char] !== undefined) {
      englishStr += bengaliDigits[char];
    } else if (/[0-9]/.test(char)) {
      englishStr += char;
    }
  }
  return englishStr ? parseInt(englishStr, 10) : null;
}

// ==========================================================================
// SEARCH & JUMP IMPLEMENTATION
// ==========================================================================
function performSearch() {
  if (!searchInput) return;
  const query = searchInput.value.trim();
  if (!query) {
    showToast('অনুগ্রহ করে কিছু লিখুন (Please type something)');
    return;
  }

  // Case 1: Check if input is a page number
  const parsedPageNumber = parseNumber(query);
  if (parsedPageNumber !== null) {
    // Look for page index with matching pg number
    let targetIndex = pages.findIndex(p => p.pg === parsedPageNumber);
    
    // If not found as pg, check if it fits in 1 to pages.length range
    if (targetIndex === -1) {
      if (parsedPageNumber >= 1 && parsedPageNumber <= pages.length) {
        targetIndex = parsedPageNumber - 1;
      }
    }

    if (targetIndex !== -1 && targetIndex !== currentPageIndex) {
      turnPage(targetIndex);
      showToast(`পৃষ্ঠা ${toBengaliNumber(parsedPageNumber)}-এ চলে যাওয়া হচ্ছে`);
      searchInput.value = '';
      searchInput.blur();
    } else if (targetIndex === currentPageIndex) {
      showToast(`আপনি ইতিমধ্যে পৃষ্ঠা ${toBengaliNumber(parsedPageNumber)}-এ আছেন`);
    } else {
      showToast(`দুঃখিত, ${toBengaliNumber(parsedPageNumber)} নম্বর পৃষ্ঠাটি পাওয়া যায়নি`);
    }
    return;
  }

  // Case 2: Full text search
  const cleanQuery = query.toLowerCase();
  
  // Helper to extract text from HTML string
  function getCleanText(html) {
    const temp = document.createElement('div');
    temp.innerHTML = html;
    return temp.textContent || temp.innerText || "";
  }

  // Check if we are continuing the same search query
  if (cleanQuery === lastSearchQuery) {
    if (lastSearchMatches.length === 0) {
      showToast(`কোনো মিল পাওয়া যায়নি: "${query}"`);
      return;
    }
    // Cycle to next match
    lastSearchMatchIndex = (lastSearchMatchIndex + 1) % lastSearchMatches.length;
    const targetIndex = lastSearchMatches[lastSearchMatchIndex];
    turnPage(targetIndex);
    showToast(`মিল ${toBengaliNumber(lastSearchMatchIndex + 1)} / ${toBengaliNumber(lastSearchMatches.length)} (পৃষ্ঠা ${toBengaliNumber(targetIndex + 1)})`);
    return;
  }

  // New query search
  const matches = [];
  pages.forEach((pageData, index) => {
    const pageText = getCleanText(pageData.html).toLowerCase();
    if (pageText.includes(cleanQuery)) {
      matches.push(index);
    }
  });

  lastSearchQuery = cleanQuery;
  lastSearchMatches = matches;
  
  if (matches.length > 0) {
    // Find the first match that is equal to or after the current page, or wrap around
    let matchIdx = matches.findIndex(idx => idx >= currentPageIndex);
    if (matchIdx === -1) matchIdx = 0;
    
    lastSearchMatchIndex = matchIdx;
    const targetIndex = matches[matchIdx];
    turnPage(targetIndex);
    showToast(`মোট ${toBengaliNumber(matches.length)}টি মিল পাওয়া গেছে। প্রথমটি পৃষ্ঠা ${toBengaliNumber(targetIndex + 1)}-এ`);
  } else {
    lastSearchMatchIndex = -1;
    showToast(`কোনো মিল পাওয়া যায়নি: "${query}"`);
  }
}

