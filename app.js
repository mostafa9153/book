function turnPage(target) {
  if (isTransitioning) return;

  let targetIndex = currentPageIndex;
  let direction = 'next';

  if (target === 'next') {
    if (currentPageIndex >= pages.length - 1) return;
    targetIndex = currentPageIndex + 1;
    direction = 'next';
  } else if (target === 'prev') {
    if (currentPageIndex <= 0) return;
    targetIndex = currentPageIndex - 1;
    direction = 'prev';
  } else if (typeof target === 'number') {
    if (target < 0 || target >= pages.length || target === currentPageIndex) return;
    targetIndex = target;
    direction = targetIndex > currentPageIndex ? 'next' : 'prev';
  } else {
    return;
  }

  isTransitioning = true;

  // Load the target content into the incoming page wrapper
  loadPage(targetIndex, incomingContent, incomingFooter);

  // Position target page based on flip direction
  if (direction === 'next') {
    incomingPage.classList.add('slide-in-right');
  } else {
    incomingPage.classList.add('slide-in-left');
  }

  // Trigger reflow to apply styling before animating
  incomingPage.offsetWidth;

  // Start transitions by applying transitioning classes
  currentPageElement.classList.add('page-transitioning');
  incomingPage.classList.add('page-transitioning');

  if (direction === 'next') {
    currentPageElement.classList.add('slide-out-left');
    incomingPage.classList.remove('slide-in-right');
  } else {
    currentPageElement.classList.add('slide-out-right');
    incomingPage.classList.remove('slide-in-left');
  }

  // Wait for transition duration (200ms matches CSS)
  setTimeout(() => {
    // Copy incoming state back to active container instantly
    currentPageIndex = targetIndex;
    loadPage(currentPageIndex, currentContent, currentFooter);

    // Clean up all animation classes to restore stable state
    currentPageElement.classList.remove('page-transitioning', 'slide-out-left', 'slide-out-right');
    incomingPage.classList.remove('page-transitioning', 'slide-in-left', 'slide-in-right');

    updateUI();
    isTransitioning = false;
  }, 200);
}

