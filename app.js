// BOOKMARK TOGGLE LOGIC
// ==========================================================================
function toggleBookmark() {
  if (currentPageIndex === bookmarkedPageIndex) {
    // Remove bookmark
    bookmarkedPageIndex = null;
    localStorage.removeItem('book_bookmark_page');
    showToast('বুকমার্ক মুছে ফেলা হয়েছে');
  } else {
    // Add/Move bookmark to current page
    bookmarkedPageIndex = currentPageIndex;
    localStorage.setItem('book_bookmark_page', currentPageIndex);
    showToast(`পৃষ্ঠা ${toBengaliNumber(currentPageIndex + 1)} বুকমার্ক করা হয়েছে`);
  }
  updateUI();
}

// ==========================================================================
// EVENT LISTENERS Setup
// ==========================================================================
function setupEventListeners() {
  // Tap/click on book halves for page navigation (NOT fullscreen toggle)
  book.addEventListener('click', (e) => {
    // Ignore clicks on interactive elements
    if (e.target.closest('.controls-overlay') ||
        e.target.closest('.fullscreen-exit-btn') ||
        e.target.closest('.bookmark-ribbon') ||
        e.target.closest('.search-toast') ||
        e.target.closest('#searchInput') ||
        e.target.closest('#searchBtn')) {
      return;
    }
    const rect = book.getBoundingClientRect();
    const clickX = e.clientX - rect.left;
    if (clickX < rect.width * 0.35) {
      turnPage('prev');
    } else if (clickX > rect.width * 0.65) {
      turnPage('next');
    }
  });

  // Swipe support for mobile
  let touchStartX = 0;
  let touchStartY = 0;
  let isSwiping = false;
  
  book.addEventListener('touchstart', (e) => {
    if (e.target.closest('.page-content') && e.target.closest('.page-content').scrollHeight > e.target.closest('.page-content').clientHeight) {
      // Let scrollable content scroll naturally
    }
    touchStartX = e.touches[0].clientX;
    touchStartY = e.touches[0].clientY;
    isSwiping = true;
  }, { passive: true });

  book.addEventListener('touchend', (e) => {
    if (!isSwiping) return;
    isSwiping = false;
    const touchEndX = e.changedTouches[0].clientX;
    const touchEndY = e.changedTouches[0].clientY;
    const diffX = touchEndX - touchStartX;
    const diffY = touchEndY - touchStartY;
    
    // Only register horizontal swipes (not vertical scrolling)
    if (Math.abs(diffX) > 50 && Math.abs(diffX) > Math.abs(diffY) * 1.5) {
      if (diffX < 0) {
        turnPage('next');
      } else {
        turnPage('prev');
      }
    }
  }, { passive: true });

  // Manual Nav Controls
  prevPageBtn.addEventListener('click', () => turnPage('prev'));
  nextPageBtn.addEventListener('click', () => turnPage('next'));

  // Bookmark Ribbon Click (on the book page)
  if (bookmarkRibbon) {
    bookmarkRibbon.addEventListener('click', (e) => {
      e.stopPropagation();
      toggleBookmark();
    });
  }

  // Go to Bookmark Button (in the controls)
  if (goToBookmarkBtn) {
    goToBookmarkBtn.addEventListener('click', (e) => {
      e.stopPropagation();
      if (bookmarkedPageIndex !== null) {
        turnPage(bookmarkedPageIndex);
        showToast(`বুকমার্ক করা পৃষ্ঠা ${toBengaliNumber(bookmarkedPageIndex + 1)}-এ যাওয়া হচ্ছে`);
      }
    });
  }

  // Search Click and Input Event Listeners
  if (searchBtn && searchInput) {
    searchBtn.addEventListener('click', (e) => {
      e.stopPropagation();
      performSearch();
    });

    searchInput.addEventListener('keydown', (e) => {
      if (e.key === 'Enter') {
        performSearch();
      }
      e.stopPropagation(); // Avoid triggering keyboard hotkeys when inside input
    });
    
    // Specifically block arrow keys when typing
    searchInput.addEventListener('keydown', (e) => {
      if (e.key === 'ArrowLeft' || e.key === 'ArrowRight') {
        e.stopPropagation();
      }
    });
  }

  // Exit Fullscreen Floating Button
  fullscreenExitBtn.addEventListener('click', () => {
    if (document.fullscreenElement) {
      document.exitFullscreen();
    } else {
      // Fallback close
      desk.classList.remove('fullscreen-active');
      isFullscreen = false;
    }
  });

  // Enter Fullscreen Button (in controls bar)
  if (fullscreenBtn) {
    fullscreenBtn.addEventListener('click', (e) => {
      e.stopPropagation();
      toggleFullscreen(null);
    });
  }

  // Keyboard navigation
  window.addEventListener('keydown', (e) => {
    // Avoid keyboard nav when user is typing in an input
    if (document.activeElement && document.activeElement.tagName === 'INPUT') {
      return;
    }

    if (e.key === 'ArrowRight') {
      turnPage('next');
    } else if (e.key === 'ArrowLeft') {
      turnPage('prev');
    } else if (e.key === 'Escape' && isFullscreen && !document.fullscreenElement) {
      // Manual fallback exit
      desk.classList.remove('fullscreen-active');
      isFullscreen = false;
    }
  });

  // ========================================================================
  // TOUCH SWIPE DETECTION
  // ========================================================================
  book.addEventListener('touchstart', (e) => {
    if (e.touches.length === 1) {
      touchStartX = e.touches[0].clientX;
      touchStartY = e.touches[0].clientY;
      wasPinching = false;
    } else if (e.touches.length === 2) {
      // Setup pinch zoom start details
      initialTouchDistance = getTouchDistance(e.touches);
      initialZoom = zoomLevel;
      wasPinching = true;
    }
  }, { passive: true });

  book.addEventListener('touchmove', (e) => {
    if (e.touches.length === 2 && initialTouchDistance !== null) {
      // Handle pinch-to-zoom scaling
      const currentDistance = getTouchDistance(e.touches);
      if (currentDistance > 0) {
        const scaleRatio = currentDistance / initialTouchDistance;
        setZoom(initialZoom * scaleRatio);
      }
    }
  }, { passive: true });

  book.addEventListener('touchend', (e) => {
    if (e.touches.length === 0) {
      // Clear pinch tracking on finger release
      initialTouchDistance = null;
    }

    if (e.changedTouches.length === 1) {
      touchEndX = e.changedTouches[0].clientX;
      touchEndY = e.changedTouches[0].clientY;
      handleSwipe();
    }
  }, { passive: true });
}

// Calculate distance between two touches for pinch-to-zoom
function getTouchDistance(touches) {
  const dx = touches[0].clientX - touches[1].clientX;
  const dy = touches[0].clientY - touches[1].clientY;
  return Math.hypot(dx, dy);
}

// Execute turn if touch movement coordinates constitute swipe
function handleSwipe() {
  if (wasPinching) return;
  const deltaX = touchEndX - touchStartX;
  const deltaY = touchEndY - touchStartY;

  // Swipe must be horizontal (X distance > threshold) and narrow vertically
  if (Math.abs(deltaX) > SWIPE_THRESHOLD && Math.abs(deltaY) < SWIPE_VERTICAL_LIMIT) {
    if (deltaX < 0) {
      turnPage('next');
    } else {
      turnPage('prev');
    }
  }
}

