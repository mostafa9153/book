let isInitialized = false;
function init() {
  if (isInitialized) return;
  isInitialized = true;
  // Load bookmark from local storage
  const savedBookmark = localStorage.getItem('book_bookmark_page');
  if (savedBookmark !== null) {
    const parsedBookmark = parseInt(savedBookmark, 10);
    if (!isNaN(parsedBookmark) && parsedBookmark >= 0 && parsedBookmark < pages.length) {
      bookmarkedPageIndex = parsedBookmark;
      currentPageIndex = parsedBookmark;
    }
  }

  loadPage(currentPageIndex, currentContent, currentFooter);
  updateUI();
  setupEventListeners();
}

function loadPage(index, contentTarget, footerTarget) {
  const pageData = pages[index];
  if (!pageData) return;

  contentTarget.innerHTML = pageData.html;
  
  if (pageData.pg !== undefined) {
    footerTarget.textContent = toBengaliNumber(pageData.pg);
  } else {
    footerTarget.textContent = '';
  }

  // Scroll to top when page changes
  contentTarget.scrollTop = 0;
}

function updateUI() {
  // Update page indicators
  pageIndicator.textContent = `${toBengaliNumber(currentPageIndex + 1)} / ${toBengaliNumber(pages.length)}`;
  
  // Update button states
  prevPageBtn.disabled = currentPageIndex === 0;
  nextPageBtn.disabled = currentPageIndex === pages.length - 1;
  
  prevPageBtn.style.opacity = prevPageBtn.disabled ? '0.3' : '1';
  nextPageBtn.style.opacity = nextPageBtn.disabled ? '0.3' : '1';

  // Update zoom values
  document.documentElement.style.setProperty('--zoom-level', zoomLevel);

  // Update bookmark ribbon visual state
  if (bookmarkRibbon) {
    if (currentPageIndex === bookmarkedPageIndex) {
      bookmarkRibbon.classList.add('active');
      bookmarkRibbon.setAttribute('title', 'বুকমার্ক সরান (Remove Bookmark)');
    } else {
      bookmarkRibbon.classList.remove('active');
      bookmarkRibbon.setAttribute('title', 'এই পৃষ্ঠাটি বুকমার্ক করুন (Bookmark this page)');
    }
  }

  // Update bookmark go-to button state
  if (goToBookmarkBtn) {
    goToBookmarkBtn.disabled = bookmarkedPageIndex === null;
    goToBookmarkBtn.style.opacity = goToBookmarkBtn.disabled ? '0.4' : '1';
    goToBookmarkBtn.style.cursor = goToBookmarkBtn.disabled ? 'not-allowed' : 'pointer';
  }
}

