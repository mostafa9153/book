function toBengaliNumber(number) {
  const bengaliDigits = ['০', '১', '২', '৩', '৪', '৫', '৬', '৭', '৮', '৯'];
  return number.toString().split('').map(digit => {
    return bengaliDigits[parseInt(digit, 10)] || digit;
  }).join('');
}

